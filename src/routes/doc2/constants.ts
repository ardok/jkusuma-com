// Ensure that there's no duplicates in terms of ID
const ALL_CARDS = [...DOC2_CARDS];
function findDuplicateIds(
  cards: Array<{ id: string; [k: string]: any }>
): string[] {
  const seen = new Set<string>();
  const duplicates: string[] = [];
  for (const card of cards) {
    if (seen.has(card.id)) {
      throw new Error(`Duplicate ID found: ${card.id}`);
      // duplicates.push(card.id);
    } else {
      seen.add(card.id);
    }
  }
  return duplicates;
}
